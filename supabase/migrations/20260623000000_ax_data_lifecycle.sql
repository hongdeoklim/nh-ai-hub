  DROP CONSTRAINT IF EXISTS knowledge_base_storage_provider_check,
  ADD CONSTRAINT knowledge_base_storage_provider_check
    CHECK (storage_provider IN ('gcs', 'supabase', 'external')),
  DROP CONSTRAINT IF EXISTS knowledge_base_ingest_status_check,
  ADD CONSTRAINT knowledge_base_ingest_status_check
    CHECK (ingest_status IN ('pending', 'processing', 'ready', 'failed', 'deleted')),
  DROP CONSTRAINT IF EXISTS knowledge_base_dify_sync_status_check,
  ADD CONSTRAINT knowledge_base_dify_sync_status_check
    CHECK (dify_sync_status IN ('pending', 'processing', 'synced', 'failed', 'deleted')),
  DROP CONSTRAINT IF EXISTS knowledge_base_retention_class_check,
  ADD CONSTRAINT knowledge_base_retention_class_check
    CHECK (retention_class IN ('temporary', 'business_record', 'training_candidate', 'permanent'));

UPDATE public.knowledge_base
SET storage_bucket = COALESCE(storage_bucket, current_setting('app.settings.gcs_bucket', true)),
    storage_object_path = COALESCE(
      storage_object_path,
      CASE
        WHEN file_url LIKE 'https://storage.googleapis.com/%/%'
          THEN regexp_replace(file_url, '^https://storage.googleapis.com/[^/]+/', '')
        WHEN file_url LIKE 'gs://%/%'
          THEN regexp_replace(file_url, '^gs://[^/]+/', '')
        ELSE NULL
      END
    );

CREATE TABLE IF NOT EXISTS public.knowledge_sync_jobs (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  knowledge_base_id uuid,
  operation text NOT NULL CHECK (operation IN ('upsert', 'delete')),
  status text NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'processing', 'done', 'failed')),
  attempts integer NOT NULL DEFAULT 0,
  available_at timestamptz NOT NULL DEFAULT now(),
  last_error text,
  snapshot jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  processed_at timestamptz
);

CREATE INDEX IF NOT EXISTS knowledge_sync_jobs_pending_idx
  ON public.knowledge_sync_jobs(status, available_at, created_at);
CREATE UNIQUE INDEX IF NOT EXISTS knowledge_sync_jobs_one_pending_idx
  ON public.knowledge_sync_jobs(knowledge_base_id, operation)
  WHERE status IN ('pending', 'processing');

